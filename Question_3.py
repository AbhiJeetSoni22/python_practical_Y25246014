a = 10
b = 3

# Arithmetic
print("Arithmetic:", a + b, a - b, a * b, a / b, a % b, a ** b)

# Relational
print("Relational:", a > b, a < b, a == b, a != b)

# Logical
print("Logical:", (a > 5 and b < 5), (a < 5 or b < 5), not(a > b))

# Bitwise
print("Bitwise:", a & b, a | b, a ^ b, ~a, a << 1, a >> 1)

# Assignment
c = a
c += b
print("Assignment (+=):", c)