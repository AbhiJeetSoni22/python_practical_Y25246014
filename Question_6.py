# Q6: Arithmetic operations on complex numbers

# Define two complex numbers
c1 = complex(2, 3)   # 2 + 3j
c2 = complex(1, 4)   # 1 + 4j

# Addition
add = c1 + c2

# Subtraction
sub = c1 - c2

# Multiplication
mul = c1 * c2

# Division
div = c1 / c2

# Display results
print("First Complex Number:", c1)
print("Second Complex Number:", c2)

print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)